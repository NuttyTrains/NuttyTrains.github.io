function ChinaFlag(){
    var id = document.getElementById('Flag'); 
    if(id.style.display == 'block')
    { id.style.display = 'none'; 
    id.style.visibility = 'hidden'; }
    else{ id.style.display = 'block'; 
    id.style.visibility = 'visible';}}

